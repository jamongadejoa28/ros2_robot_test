// clang-format on

Ili9341Lcd::Ili9341Lcd(const Config& config) : config_(config) {
  rgb565_buffer_.resize(config_.width * config_.height * 2);
}

Ili9341Lcd::~Ili9341Lcd() {
  if (spi_fd_ != -1) {
    // Display OFF command
    SpiWriteCommand(0x28);
    // Backlight OFF
    digitalWrite(config_.bl_pin, LOW);
    close(spi_fd_);
    spi_fd_ = -1;
  }
}

bool Ili9341Lcd::Init() {
  if (wiringPiSetupGpio() < 0) {
    std::cerr << "Ili9341Lcd: Failed to setup wiringPi GPIO\n";
    return false;
  }

  pinMode(config_.dc_pin, OUTPUT);
  pinMode(config_.rst_pin, OUTPUT);
  pinMode(config_.bl_pin, OUTPUT);

  digitalWrite(config_.bl_pin, HIGH); // Backlight ON

  spi_fd_ = wiringPiSPISetup(config_.spi_channel, config_.spi_speed);
  if (spi_fd_ < 0) {
    std::cerr << "Ili9341Lcd: Failed to setup SPI\n";
    return false;
  }

  // Hardware Reset
  digitalWrite(config_.rst_pin, HIGH);
  delay(10);
  digitalWrite(config_.rst_pin, LOW);
  delay(10);
  digitalWrite(config_.rst_pin, HIGH);
  delay(120);

  // ILI9341 initialization sequence
  SpiWriteCommand(0x01); // Software Reset
  delay(150);
  
  SpiWriteCommand(0x11); // Sleep Out
  delay(120);

  SpiWriteCommand(0x3A); // Pixel Format Set
  SpiWriteData(0x55);    // 16-bit RGB 5-6-5 format

  SpiWriteCommand(0x36); // Memory Access Control (MADCTL)
  SpiWriteData(0x68);    // Landscape: MX=1, MV=1, BGR=1

  SpiWriteCommand(0x21); // Display Invert ON (Fixes white bg / green faces)

  SpiWriteCommand(0x29); // Display ON

  // Clear to black on startup
  ClearScreen(0x0000);

  std::cout << "ILI9341 initialized on SPI channel " << config_.spi_channel << "\n";
  return true;
}

// ---- SPI helpers ----

void Ili9341Lcd::SpiWriteCommand(uint8_t cmd) {
  digitalWrite(config_.dc_pin, LOW); // command mode
  wiringPiSPIDataRW(config_.spi_channel, &cmd, 1);
}

void Ili9341Lcd::SpiWriteData(uint8_t data) {
  digitalWrite(config_.dc_pin, HIGH); // data mode
  wiringPiSPIDataRW(config_.spi_channel, &data, 1);
}

void Ili9341Lcd::SpiWriteDataBlock(const uint8_t* data, size_t len) {
  digitalWrite(config_.dc_pin, HIGH); // data mode
  
  constexpr size_t kChunkSize = 4096;
  size_t offset = 0;
  
  while (offset < len) {
    size_t chunk = std::min(kChunkSize, len - offset);
    wiringPiSPIDataRW(config_.spi_channel, const_cast<uint8_t*>(data + offset), chunk);
    offset += chunk;
  }
}

void Ili9341Lcd::SetWindow(int x0, int y0, int x1, int y1) {
  SpiWriteCommand(0x2A); // CASET
  SpiWriteData(static_cast<uint8_t>(x0 >> 8));
  SpiWriteData(static_cast<uint8_t>(x0 & 0xFF));
  SpiWriteData(static_cast<uint8_t>(x1 >> 8));
  SpiWriteData(static_cast<uint8_t>(x1 & 0xFF));

  SpiWriteCommand(0x2B); // PASET
  SpiWriteData(static_cast<uint8_t>(y0 >> 8));
  SpiWriteData(static_cast<uint8_t>(y0 & 0xFF));
  SpiWriteData(static_cast<uint8_t>(y1 >> 8));
  SpiWriteData(static_cast<uint8_t>(y1 & 0xFF));
}

// ---- Public drawing methods ----

void Ili9341Lcd::ClearScreen(uint16_t color_rgb565) {
  SetWindow(0, 0, config_.width - 1, config_.height - 1);
  SpiWriteCommand(0x2C); // RAMWR

  // Fill entire screen with the specified color
  size_t total_pixels = static_cast<size_t>(config_.width * config_.height);
  std::vector<uint8_t> buf(total_pixels * 2);
  uint8_t hi = static_cast<uint8_t>(color_rgb565 >> 8);
  uint8_t lo = static_cast<uint8_t>(color_rgb565 & 0xFF);
  for (size_t i = 0; i < total_pixels; ++i) {
    buf[i * 2]     = hi;
    buf[i * 2 + 1] = lo;
  }
  SpiWriteDataBlock(buf.data(), buf.size());
}

void Ili9341Lcd::DrawChar(int x, int y, char c, uint16_t fg, uint16_t bg) {
  if (c < 32 || c > 126) c = ' ';
  int idx = c - 32;

  // Clip check
  if (x + 8 > config_.width || y + 8 > config_.height) return;

  SetWindow(x, y, x + 7, y + 7);
  SpiWriteCommand(0x2C);

  uint8_t pixel_buf[8 * 8 * 2]; // 8x8 pixels, 2 bytes each
  int pos = 0;

  for (int row = 0; row < 8; ++row) {
    uint8_t bits = kFont8x8[idx][row];
    for (int col = 0; col < 8; ++col) {
      uint16_t color = (bits & (0x80 >> col)) ? fg : bg;
      pixel_buf[pos++] = static_cast<uint8_t>(color >> 8);
      pixel_buf[pos++] = static_cast<uint8_t>(color & 0xFF);
    }
  }

  SpiWriteDataBlock(pixel_buf, sizeof(pixel_buf));
}

void Ili9341Lcd::DrawText(int x, int y, const std::string& text,
                          uint16_t fg, uint16_t bg) {
  int cx = x;
  for (char c : text) {
    if (cx + 8 > config_.width) break; // Line overflow
    DrawChar(cx, y, c, fg, bg);
    cx += 8; // 8px wide per character
  }
}

void Ili9341Lcd::DrawFrame(const uint8_t* buffer, size_t size) {
  size_t expected_rgb888 = static_cast<size_t>(config_.width * config_.height * 3);
  if (size != expected_rgb888) {
    std::cerr << "DrawFrame: expected " << expected_rgb888 << " bytes (RGB888), got " << size << "\n";
    return;
  }

  // Convert RGB888 to RGB565
  for (size_t i = 0, j = 0; i < size; i += 3, j += 2) {
    uint8_t r = buffer[i];
    uint8_t g = buffer[i + 1];
    uint8_t b = buffer[i + 2];

    uint16_t color = ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3);
    rgb565_buffer_[j] = static_cast<uint8_t>(color >> 8);
    rgb565_buffer_[j + 1] = static_cast<uint8_t>(color & 0xFF);
  }

  SetWindow(0, 0, config_.width - 1, config_.height - 1);
  SpiWriteCommand(0x2C);
  SpiWriteDataBlock(rgb565_buffer_.data(), rgb565_buffer_.size());
}

void Ili9341Lcd::DrawFrameRgb565(const uint8_t* buffer, size_t size) {
  size_t expected_rgb565 = static_cast<size_t>(config_.width * config_.height * 2);
  if (size != expected_rgb565) {
    std::cerr << "DrawFrameRgb565: expected " << expected_rgb565 << " bytes, got " << size << "\n";
    return;
  }

  SetWindow(0, 0, config_.width - 1, config_.height - 1);
  SpiWriteCommand(0x2C);
  SpiWriteDataBlock(buffer, size);
}

